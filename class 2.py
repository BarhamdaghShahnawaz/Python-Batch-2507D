# lesson 1 
# Data Types 
# "Data type se yeh pata chalta hai ke kisi variable ke andar kaunsa type ka data rakha gaya hai.
#  Har type ka data system mein alag tareeqe se behave karta hai."


#  1. String 
# String ka matlab hota hai text ya lafz. Ye hamesha double quotes (" ") ya 
# single quotes (' ') mein likhe jate hain.

# Example: 
# name = "Ali" 
# message = 'Hello, World!'
# print(name) 


#  2. Integer 
# Integer simple poore number hote hain — jaise 1, 5, -10. Isme decimal nahi hota. 

#  Example: 
# age = 18 
# print(age) 


#  3. Float 
# Float ka matlab hota hai decimal number — jaise 3.14, 99.9, 2.5 

# Example:
# price = 19.99 
# print(price)


# 4. Boolean 
# Boolean sirf do hi value hoti hai: True ya False
# Ye mostly condition check karne mein kaam aata hai.

# Example:
# is_student = True
# print(is_student)


#  5. None 
# Jab kisi variable mein kuch bhi value nahi di jati, to hum usmein None likhte hain.
# Iska matlab hota hai: "abhi khaali hai". 

# Example:
# student_name = None
# print(student_name) 


