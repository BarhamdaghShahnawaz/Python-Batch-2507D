# Logical Operators 
# ogical operators aise operators hote hain jo true ya false (Boolean values) ke basis par
#  decision lete hain. Inka use hum conditions ke sath karte hain — jaise if, while, etc.

# | Operator | Name        | Use karta hai...                                         |
# | -------- | ----------- | -------------------------------------------------------- |
# | `and`    | Logical AND | Dono condition true honi chahiye                         |
# | `or`     | Logical OR  | Koi ek condition true ho to chalega                      |
# | `not`    | Logical NOT | Condition ka ulta karta hai (true → false, false → true) |
